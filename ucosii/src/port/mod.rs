type BOOLEAN = bool;   // Unsigned  8 bit quantity
type INT8U = u8;     // Unsigned  8 bit quantity
type INT8S = i8;     // Signed    8 bit quantity
type INT16U = u16;   // Unsigned 16 bit quantity
type INT16S = i16;   // Signed   16 bit quantity
type INT32U = u32;   // Unsigned 32 bit quantity
type INT32S = i32;   // Signed   32 bit quantity
type FP32 = f32;     // Single precision floating point
type FP64 = f64;     // Double precision floating point

type OS_CPU_SR = u32; // Define size of CPU status register (PSR = 32 bits)